package lekcja4_metody_i_konstruktory.zad2;

public class TimeConverter {

    int hoursToMinutes(int hours) {
        return hours * 60;
    }

    int minutesToSeconds(int minutes) {
        return minutes * 60;
    }

    int secondsToMiliseconds(int seconds) {
        return seconds * 1000;
    }
}
